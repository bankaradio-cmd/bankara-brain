def main():
    conn = sqlite3.connect(DB_PATH)
    cur = conn.cursor()

    # Check no v5 record exists yet
    cur.execute(
        "SELECT COUNT(*) FROM brain_deep_analysis WHERE asset_id=? AND version='v5'",
        (ASSET_ID,),
    )
    if cur.fetchone()[0] > 0:
        print("ERROR: v5 record already exists for this asset. Aborting.")
        conn.close()
        return

    # Get gemini_analysis from v4 record
    cur.execute(
        "SELECT gemini_analysis FROM brain_deep_analysis WHERE asset_id=? AND version='v4'",
        (ASSET_ID,),
    )
    row = cur.fetchone()
    gemini_text = row[0] if row else None

    now = datetime.now().strftime("%Y-%m-%d %H:%M:%S")

    cur.execute(
        """INSERT INTO brain_deep_analysis (asset_id, version, gemini_analysis, opus_analysis, created_at, updated_at)
           VALUES (?, 'v5', ?, ?, ?, ?)""",
        (ASSET_ID, gemini_text, OPUS_ANALYSIS, now, now),
    )
    conn.commit()

    # Verify
    cur.execute(
        "SELECT id, version, LENGTH(opus_analysis) FROM brain_deep_analysis WHERE asset_id=? AND version='v5'",
        (ASSET_ID,),
    )
    row = cur.fetchone()
    print(f"Inserted: id={row[0]}, version={row[1]}, opus_analysis length={row[2]} chars")

    conn.close()

if __name__ == "__main__":
    main()
